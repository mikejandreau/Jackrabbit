var showScrollToTop = function () {
  var y = window.scrollY;
  if (y >= 300) {
    $("#scrollUpButton").addClass("show");
  } else {
    $("#scrollUpButton").removeClass("show");
  }
};
window.addEventListener("scroll", showScrollToTop);

